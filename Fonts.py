import tkinter as tk
from tkinter import font as tkFont

def Helvetica(fontSize):
    return tkFont.Font(family='Helvetica', size=fontSize, weight='bold')

def Times(fontSize):
    return tkFont.Font(family='Times', size=fontSize, weight='bold')

def Calibre(fontSize):
    return tkFont.Font(family='Calibre', size=fontSize, weight='bold')
